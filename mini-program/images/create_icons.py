#!/usr/bin/env python3
"""创建简单的 tabBar 图标占位文件"""

import os

# 图标列表
icons = [
    "home.png", "home-active.png",
    "monitor.png", "monitor-active.png",
    "alert.png", "alert-active.png",
    "profile.png", "profile-active.png"
]

# 创建一个最小的有效 PNG 文件 (1x1 像素，透明)
# PNG 文件头 + IHDR 数据块 + IDAT 数据块 + IEND 数据块
png_data = bytes([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D,  # IHDR length
    0x49, 0x48, 0x44, 0x52,  # IHDR
    0x00, 0x00, 0x00, 0x01,  # width: 1
    0x00, 0x00, 0x00, 0x01,  # height: 1
    0x08, 0x06, 0x00, 0x00, 0x00,  # 8-bit RGBA
    0x1F, 0x15, 0xC4, 0x89,  # IHDR CRC
    0x00, 0x00, 0x00, 0x0D,  # IDAT length
    0x49, 0x44, 0x41, 0x54,  # IDAT
    0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00, 0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x05,
    0xFE, 0xD7, 0xB0,  # IDAT CRC
    0x00, 0x00, 0x00, 0x00,  # IEND length
    0x49, 0x45, 0x4E, 0x44,  # IEND
    0xAE, 0x42, 0x60, 0x82   # IEND CRC
])

for icon in icons:
    with open(icon, 'wb') as f:
        f.write(png_data)
    print(f"Created: {icon}")

print(f"\nAll {len(icons)} icons created successfully!")
